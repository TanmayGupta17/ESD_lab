	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=LSB
	LDR R1,=MSB
	MOV R4,#10
BACK LDR R2,[R0],#4		;LSB
	LDR R3,[R1],#4		;MSB
						      ;R5 LSB SUM
						      ;R6 MSB SUM
	ADDS R5,R2
	ADC R6,R3
	SUBS R4,#1
	BNE BACK
	LDR R9,=DST
	STR R5,[R9],#4
	STR R6,[R9],#4
LSB DCD 0X1,0X2,0X3,0X4,0X5,0X6,0X7,0X8,0X9,0XA
MSB DCD 0XA,0X9,0X8,0X7,0X6,0X5,0X4,0X3,0X2,0X1
	AREA mydata,DATA,READWRITE
DST DCD 0,0
	END
