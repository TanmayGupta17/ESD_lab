	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=NO1
	LDR R1,=NO2
	LDR R2,=DST
	LDR R3,[R0]
	LDR R4,[R1]
GCD CMP R3,R4
	BEQ EXIT
	BHI G1
	BLO L1
G1	SUB R3,R4
L1	SUB R4,R3
	B GCD
EXIT STR R3,[R2]
NO1 DCD 0X3
NO2 DCD 0X6
	AREA mydata,DATA,READWRITE
DST DCD 0
	END
