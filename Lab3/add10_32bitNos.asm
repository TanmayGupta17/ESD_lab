	AREA RESET,CODE,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=SRC
	MOV R10,#10
	LDR R1,=DST
	MOV R2,#0            ;R3 IS CARING THE CARRY NO
	LDR R3,=0X0          ;CAN'T USE MOVE CAUSE ITS RESTRICTED TO 8 BITS ONLY AND WE CANT MOVE THE ALL THE 32 BITS IN THAT REGISTER
BACK LDR R3,[R0],#4
	ADDS R4,R3            
	ADC R2,#0            ;ADD WITH CARRY , ADD THE CARRY IN THE R2 REGISTER
	SUBS R10,#1
	BNE BACK
	STR R2,[R1],#4        ;FIRST WE STORE THE CARRY THEN THE NO
	STR R4,[R1]
SRC DCD 0X11111111,0X22222222,0X33333333,0X44444444,0X55555555,0X66666666,0X77777777,0X88888888,0X99999999,0XAAAAAAAA
	AREA mydata,DATA,READWRITE
DST DCD 0,0,0,0,0,0,0,0,0
	END
